public class Day2GitGithub {

    /*

    1) git init : Local repo oluşturmak için yani ".git" ile klasörümüzün içindeki dosyaları
    ilişkilendirmek için kullanılır.

    2) git add : Working Space'den (yani yerel) dosyalarımı Staging Area (commit'leme bekleme
    salonu)'ya gönderir.

    3) git status: Working Space ve Spacing Area'daki durumu gösterir.

    4) git commit -m "mesaj" : Staging Area'dan Commit Store'a dosyaları gönderir. Böylelikle "Commit"
    yani versiyon/sürüm oluşturulur.

    5) git push : Uzak Repo (Remote-Github)'ya göndermek için kullanılır. "git push" komutunu direkt
    kullanmak istersek bir kez;

            git remote add origin +adres
            git push -u origin master

            NOT: Yukaridaki iki komutu tek seferde kullandiktan sonra ikinci commit'lerim için
            sadece git push kullaniriz.

     */


}
